import Head from 'next/head'
import Image from 'next/image'
import { useState, useEffect, useMemo } from 'react'
import { useMetaMask } from '../hooks/useMetaMask'
import { collection } from 'firebase/firestore'
import { firestore } from '../firebase/clientApp'
import { useCollection } from 'react-firebase-hooks/firestore'

const Home = () => {
  const { accounts, chainId, balance, message, disableButton, isConnected, connect, disconnect } = useMetaMask()
  // const [loading, setLoading] = useState(true)
  // const [success, setSuccess] = useState('')
  // const [error, setError] = useState('')
  const [whitelistUsersCount, setWhitelistUserCount] = useState(0)

  const [whitelist, whitelistLoading, whitelistError] = useCollection(
    collection(firestore, 'whitelist'),
    {}
  )

  // Get the number of users in the whitelist
  useEffect(() => {
    if (whitelist?.docs?.length > 0) setWhitelistUserCount(whitelist.docs.length)
  }, [whitelist])

  const styles = {
    container: "flex flex-col items-center h-screen"
  }

  const Button = ({ disabled, onClick }) => {
    const styles = `
    ${disabled ? 'bg-gray-400' : 'bg-[#F4841F] hover:bg-[#763E1A]'}
    font-medium text-white py-2 px-4 rounded
    `
    return (
      <button
        className={styles}
        disabled={disabled}
        onClick={onClick}
      >
        Connect to Metamask
      </button>
    )
  }

  const InfosAccount = ({ isConnected, message, balance }) => {
    return (
      <>
        {!isConnected && (
          <Button
            onClick={connect}
            disabled={disableButton}
          />
        )}
        {message && <p>{message}</p>}
        {isConnected && <p>Your Balance : {balance} eth</p>}
        {isConnected && balance < 0.3 && <p>You don't have enough ETH on your account to go on our whitelist.</p>}
      </>
    )
  }

  return (
    <div className="flex flex-col items-center justify-center h-screen bg-gradient-to-tr from-indigo-500 to-blue-500 text-white">
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <InfosAccount {...{ isConnected, message, balance }} />
    </div>
  )
}

export default Home
