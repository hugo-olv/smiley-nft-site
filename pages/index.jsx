import Head from 'next/head'
import Image from 'next/image'
import { useState, useEffect } from 'react'
import { useMetaMask } from '../hooks/useMetaMask'

const Home = () => {
  const { accounts, chainId, balance, message, enableButton, isConnected, connect } = useMetaMask()
  const [loading, setLoading] = useState(true)
  const [success, setSuccess] = useState('')
  const [error, setError] = useState('')


  const styles = {
    container: "flex flex-col items-center"
  }

  const Button = ({ variant, disabled, onClick }) => {
    const styles = `
    ${variant === 'connect' && !disabled && 'bg-blue-500 hover:bg-blue-700'} 
    ${variant === 'disconnect' && !disabled && 'bg-red-500 hover:bg-red-700'}
    ${disabled && 'bg-gray-400'}
    font-bold text-white py-2 px-4 rounded
    `
    return (
      <button
        className={styles}
        disabled={disabled}
        onClick={onClick}
      >
        {variant === 'connect' ? 'Connect to MetaMask' : 'Disconnect to MetaMask'}
      </button>
    )
  }

  return (
    <div className={styles.container}>
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <Button
        variant={isConnected ? 'connect' : 'disconnect'}
        onClick={isConnected ? connect : disconnect}
        disabled={!enableButton}
      />
      <p>{message}</p>
    </div>
  )
}

export default Home
